TokenTreeCpp::TokenTreeCpp(const std::filesystem::path& src_file, std::shared_ptr<TokenTree::Matcher> matcher) :
    TokenTree()
{
    // Tokenize
    CppPda pda(_root.get());
    line_t line = 0;
    Token* buffer = nullptr;

    { // From token list to token tree
        //fs::path temp("___temp_cleaner.txt");
        //std::ofstream ofs(temp);
        //_getRawStream(temp, matcher);

        auto raw_stream(_getRawStream(src_file, matcher));
        std::cout << src_file << std::endl;
        std::cout << "Tree Creating" << std::endl;
        for (auto& token : raw_stream) {
            
            Token* tok = &token;
            std::cout << tok->name << ' ' << tok->loc << '\n';
            if (token.type < Token::Type::OTHERWISE &&
                (token.type < Token::Type::ELSE || Token::Type::AMP <= token.type)) {

                if (line != token.loc) {

                    _stream.emplace_back();
                    line = token.loc;
                }

                tok = &_stream.rbegin()->emplace_back(token.type, line, token.name);
            }
            tok->root = _root.get();
            
            pda.trans(buffer, tok);
            buffer = tok;
        }
        std::cout << "before trans";
        pda.trans(buffer, nullptr);
        std::cout << "after trans";
    }
    _setIndexr();
    return;
    
    if (!pda.isTerminated(_root.get())) {

        std::cerr << "Incomplete token tree\n";
        throw std::logic_error(src_file);
    }
}



