std::list<Token> TokenTreeCpp::_getRawStream(const std::filesystem::path& path, std::shared_ptr<TokenTree::Matcher> matcher) const
{
    // Erase header info
    _eraseInclude(path);
    // clang++ dump-tokens
    std::system(Command::DUMP_COMMAND);
    std::remove(Command::TEMPORARY_CPP);

    // Read .txt file
    std::ifstream ifs(Command::TEMPORARY_TXT);
    ifs.seekg(0, std::ios::end);
    auto size = ifs.tellg();

    char* buf = (char*)std::malloc(size * sizeof(char));
    if (!buf) {

        std::remove(Command::TEMPORARY_TXT);
        throw std::range_error("malloc failed");
    }

    ifs.seekg(0);
    ifs.read(buf, size);
    ifs.close();
    std::remove(Command::TEMPORARY_TXT);


    // Tokenize
    std::list<Token> stream;
    for (const char* pos = buf; ; ++pos) {

        // Read buf
        const char* start = pos;
        for (; *pos != ' '; ++pos) {}
        Token::Type ttype = matcher->match(std::string(start, pos - start));

        // If End Of File, break
        if (ttype == Token::Type::eof)
            break;
        
        // Set token name
        for (; *pos != '\''; ++pos) {}
        start = ++pos;
        for (; *pos != '\''; ++pos) {}
        std::string name(start, pos - start);

        // Push token
        for (; *pos != ':'; ++pos) {}
        stream.emplace_back(ttype, static_cast<line_t>(std::atoll(pos + 1)), name);

        // Move to next line
        for (; *pos != '\n'; ++pos) {}
    }

    std::free(buf);
    return stream;
}
